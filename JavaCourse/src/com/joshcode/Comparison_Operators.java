package com.joshcode;

public class Comparison_Operators {
    public static void main(String[] args){
        // Comparison Operators

        // < <= > >= == !=

        System.out.println(10 < 10);
        System.out.println(10 <= 10);
        System.out.println(10 > 10);
        System.out.println(10 >= 10);
        System.out.println(10 == 10);
        System.out.println(10 != 10);
        System.out.println();
        boolean isAdult = 16 >= 16;
        System.out.println(isAdult);





    }
}
